let vm = new Vue({
  el: '#app',
  data: {
    mode: 'pc',
    expand: false,
    category_list: ['交通', '飲料・食品', '美容・ヘルスケア・健康', '人材', '印刷・出版', 'メディア', '金融・証券・保険'],
    color_list: ['Red', 'Pink', 'Purple', 'Blue', 'Light Blue', 'Green', 'Yellow', 'Orange', 'Brown', 'Cream', 'Black', 'White', 'Colorful'],
    web_list: default_web_list,
  },
  computed: {
    returnWidth: function(){

    }
  },
  created: function(){
    const webList = document.querySelector('.web-list');
    webList.style.width = `${this.web_list.length * 240}px`;

  },
  methods: {
    filterByCategory: function(index){
      let acctive_category = this.category_list[index];
      this.web_list = default_web_list;
      this.web_list = this.web_list.filter(function(item){
        return item.category == acctive_category;
      });
      document.body.classList.toggle("is-open");
    },
    filterByColor: function(index) {
      let acctive_color = this.color_list[index];
      this.web_list = default_web_list;
      this.web_list = this.web_list.filter(function(item){
        let l = item.color.length;
        for (let i = 0; i < l; i++){
          if (item.color[i] == acctive_color) {
            console.log('ok');
            return item.color[i] == acctive_color;
          }
        }
      });
      document.body.classList.toggle("is-open");
    },
    openMenu: function() {
      document.body.classList.toggle("is-open");
    },
    expandImage: function(index) {
      this.expand = true;
      const expand_image =  document.createElement('img');
      expand_image.classList.add('expand-image');
      if (this.mode == 'pc') {
        expand_image.src = `images/${this.web_list[index].image_pc}`;
      } else if (this.mode == 'sp') {
        expand_image.src = `images/${this.web_list[index].image_sp}`;
      }
      document.querySelector('.app-content').classList.add('is-blur');
      document.querySelector('.expand-container').appendChild(expand_image);
    },
    closeExpand: function() {
      document.querySelector('.app-content').classList.remove('is-blur');
      let expand_image = document.querySelector('.expand-container').lastElementChild;
      document.querySelector('.expand-container').removeChild(expand_image);
      this.expand = false;
    },
    mouesDown: function(e) {
      const knob = document.querySelector('.knob');
      const scroll_bar = document.querySelector('.scroll-bar');
      let knob_width = knob.clientWidth;
      let start_pos = scroll_bar.offsetLeft;
      let min_pos = 0;
      let max_pos = scroll_bar.clientWidth - knob_width;;
      let mouse_pos;
      let btn_pos;
      let documentMouseMove;
      let documentMouseUp;

      documentMouseMove = document.onmousemove;
      documentMouseUp = document.onmouseup;
      document.onmousemove = function(e) {
        mouse_pos = e.pageX - start_pos;
        knob.style.left = `${mouse_pos}px`;
        if (mouse_pos <= min_pos) {
          knob.style.left = `${min_pos}px`;
        }
        if (max_pos <= mouse_pos) {
          knob.style.left = `${max_pos}px`;
        }
      };
      return document.onmouseup = function() {
          document.onmousemove = documentMouseMove;
          return document.onmouseup = documentMouseUp;
      };
    }
  }
})
